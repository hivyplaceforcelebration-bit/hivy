// Deterministic CTA picker based on slug hash
function getCTA(slug: string): string {
  let hash = 0;
  for (let i = 0; i < slug.length; i++) {
    hash = ((hash << 5) - hash + slug.charCodeAt(i)) | 0;
  }
  return ctaWords[Math.abs(hash) % ctaWords.length];
}

function toTitleCase(str: string): string {
  return str
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

function createKeyword(
  slug: string,
  category: string,
  customTitle?: string,
  customDesc?: string
): ExpandedKeyword {
  const baseSlug = slug.replace(/-in-surat$/, "");
  const title = customTitle || toTitleCase(baseSlug);
  const cta = getCTA(slug);

  return {
    slug,
    title,
    h1: `${title} in Surat`,
    metaTitle: `${title} in Surat | ${cta} — HIVY`,
    metaDescription:
      customDesc ||
      `Book ${title.toLowerCase()} in Surat at HIVY. Private venue, romantic setup, cake & dinner included. Starting ₹5100. Call +91 9727027278.`,
    category,
  };
}

