// Get related keywords for internal linking (same category + cross-category)
export function getRelatedKeywords(
  slug: string,
  count: number = 8
): ExpandedKeyword[] {
  const current = keywordMap.get(slug);
  if (!current) return allExpandedKeywords.slice(0, count);

  // Same category keywords
  const sameCategory = allExpandedKeywords.filter(
    (kw) => kw.category === current.category && kw.slug !== slug
  );

  // Different category keywords
  const diffCategory = allExpandedKeywords.filter(
    (kw) => kw.category !== current.category
  );

  // Pick 5 from same category + 3 from different (deterministic via hash)
  let hash = 0;
  for (let i = 0; i < slug.length; i++) {
    hash = ((hash << 5) - hash + slug.charCodeAt(i)) | 0;
  }
  const absHash = Math.abs(hash);

  const samePicks: ExpandedKeyword[] = [];
  const sameCatLen = sameCategory.length;
  for (let i = 0; i < Math.min(5, sameCatLen); i++) {
    samePicks.push(sameCategory[(absHash + i * 7) % sameCatLen]);
  }

  const diffPicks: ExpandedKeyword[] = [];
  const diffCatLen = diffCategory.length;
  for (let i = 0; i < Math.min(3, diffCatLen); i++) {
    diffPicks.push(diffCategory[(absHash + i * 11) % diffCatLen]);
  }

  // Deduplicate
  const seen = new Set<string>();
  const result: ExpandedKeyword[] = [];
  for (const kw of [...samePicks, ...diffPicks]) {
    if (!seen.has(kw.slug) && kw.slug !== slug) {
      seen.add(kw.slug);
      result.push(kw);
    }
  }

  return result.slice(0, count);
}

